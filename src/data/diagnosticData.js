// Determine type based on L, C, R, E scores (LCRE Gear Friction Mapping Algorithm)
export function determineDiagnosticType(scores) {
  const { L, C, R, E } = scores;
  
  // 1. Calculate standard values
  const maxVal = Math.max(L, C, R, E);
  const minVal = Math.min(L, C, R, E);
  const diffMaxMin = maxVal - minVal;
  
  // 2. Check Balanced type (Range <= 15)
  if (diffMaxMin <= 15) {
    const average = (L + C + R + E) / 4;
    // High average vs Low average
    if (average >= 20) {
      return diagnosticTypes.frogAnalyst; // 深思の神官 (frogAnalyst IDを流用)
    } else {
      return diagnosticTypes.balancedThinker; // 野生のトリックスター (balancedThinker IDを流用)
    }
  }
  
  // 3. Find 1st and 2nd maximums
  const sorted = [
    { key: "L", val: L },
    { key: "C", val: C },
    { key: "R", val: R },
    { key: "E", val: E }
  ].sort((a, b) => b.val - a.val);
  
  const m1 = sorted[0];
  const m2 = sorted[1];
  
  // 4. Check Single Overdrive (M1 - M2 >= 30)
  if (m1.val - m2.val >= 30) {
    if (m1.key === "L") return diagnosticTypes.psycho; // 冷徹なデータ・アーキテクト
    if (m1.key === "C") return diagnosticTypes.nitpicker; // 絶対防御のインスペクター
    if (m1.key === "R") return diagnosticTypes.radicalOrigin; // 宇宙を彷徨うそもそも星人
    if (m1.key === "E") return diagnosticTypes.runawayTrain; // 愛が溢れるエモーション・マグネット
  }
  
  // 5. Dual Dilemma (葛藤型)
  const topTwoKeys = [m1.key, m2.key].sort().join(""); // Sort keys to easily compare
  
  if (topTwoKeys === "EL") return diagnosticTypes.monster; // 認知的不協和バグ (EL ➡ monster IDを流用)
  if (topTwoKeys === "CE") return diagnosticTypes.sentimentalWatcher; // マインドリーディングバグ [NEW]
  if (topTwoKeys === "CL") return diagnosticTypes.judgmentBrain; // 分析麻痺バグ [NEW]
  if (topTwoKeys === "ER") return diagnosticTypes.pureDreamer; // 希望的観測バグ [NEW]
  if (topTwoKeys === "CR") return diagnosticTypes.philosopher; // シニカルバグ (CR ➡ philosopher IDを流用)
  if (topTwoKeys === "LR") return diagnosticTypes.visionary; // 机上の空論バグ [NEW]
  
  // Fallback
  return diagnosticTypes.balancedThinker;
}
